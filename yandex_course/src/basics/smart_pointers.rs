// Практическое задание 1: использование RefCell для изменения данных во внутренней структуре
// Напишите функцию add_value, которая принимает RefCell<Vec<i32>> и добавляет в вектор число. 


fn add_value(cell_vector: &RefCell<Vec<i32>>, value: i32) {
    cell_vector.borrow_mut().push(value);
}

pub fn ref_cell_add_value() {
    let numbers = RefCell::new(Vec::new());

    add_value(&numbers, 10);
    add_value(&numbers, 20);
    add_value(&numbers, 30);

    // Проверка корректности
    let borrowed = numbers.borrow();
    assert_eq!(&*borrowed, &[10, 20, 30]);

    println!("Все проверки прошли! numbers = {:?}", *borrowed);
}

/*** Rc<RefCell<T>> ***/
// Rc<RefCell<T>> — совместное владение с изменяемостью
// Комбинация Rc<RefCell<T>> позволяет иметь несколько владельцев данных, которые могут изменять эти данные:

#[derive(Debug)]
enum ListRcRefCell {
    ConsRcRefCell(Rc<RefCell<i32>>, Rc<ListRcRefCell>),
    NilRcRefCell,
}

use ListRcRefCell::{ConsRcRefCell, NilRcRefCell};

pub fn rc_refcell_example () {
    let value = Rc::new(RefCell::new(5));
    
    let a = Rc::new(ConsRcRefCell(Rc::clone(&value), Rc::new(NilRcRefCell)));  
    let b = ConsRcRefCell(Rc::new(RefCell::new(3)), Rc::clone(&a));
    let c = ConsRcRefCell(Rc::new(RefCell::new(4)), Rc::clone(&a));
    
    // Изменяем значение 
    *value.borrow_mut() += 10;
    
    println!("a после изменения = {:?}", a);
    println!("b после изменения = {:?}", b);
    println!("c после изменения = {:?}", c);
}


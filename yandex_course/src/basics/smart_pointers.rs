/*** RefCell<T> — безопасная внутренняя изменяемость ***/

// RefCell<T> позволяет изменять данные через неизменяемую ссылку, но проверяет правила заимствования во время выполнения. 
// Его можно представить как книгу в читальном зале:
//  - Можно выдать сколько угодно копий для чтения одновременно (несколько &T).
//  - Но если кто-то взял книгу для правки карандашом (одна &mut T), зал запрещает другим читать.
//  - Если нарушить правила — библиотекарь (RefCell) поднимет скандал (panic в runtime).

// Методы RefCell<T>:

//     borrow() — возвращает Ref<T> (неизменяемое заимствование).
//     borrow_mut() — возвращает RefMut<T> (изменяемое заимствование).

// RefCell<T> может без особых нюансов работать с любыми типами.

use std::cell::RefCell;
pub fn ref_cell_example() {
    let int_value = RefCell::new(5);
    
    // Неизменяемое заимствование
    {
        let borrowed = int_value.borrow();
        println!("Значение: {}", *borrowed); // 5
    } // borrowed выходит из области видимости
    
    // Изменяемое заимствование
    {
        let mut borrowed_mut = int_value.borrow_mut();
        *borrowed_mut += 10;
    } // borrowed_mut выходит из области видимости
    
    println!("Новое значение: {}", *int_value.borrow()); // 15
}


// Главное про указатели
// Указатель — это переменная, которая содержит адрес в памяти. 
// Этот адрес «ссылается на» или «указывает на» некоторые другие данные. 
// 
// 1. Самый распространённый вид указателей в Rust — это ссылки. 
//    Ссылки обозначаются символом & и заимствуют значение, на которое они указывают. 
//    У них нет никаких специальных возможностей, кроме ссылки на данные, и они не требуют дополнительных ресурсов.

// 2. Умные указатели (smart pointers), в отличие от обычных ссылок, являются структурами данных. Они не только действуют как указатель, но также имеют дополнительные метаданные и возможности. 
//    Rust с его концепцией владения и заимствования имеет дополнительное различие между ссылками и умными указателями:
//    в то время как ссылки только заимствуют данные, умные указатели часто владеют данными, на которые указывают.
//    В Rust стандартная библиотека предоставляет множество умных указателей, которые обеспечивают функциональность, выходящую за рамки обычных ссылок.
//    Вот основной их список:

//     Box<T> — для размещения данных в куче.
//     Rc<T> — для совместного владения (reference counting).
//     Arc<T> — аналог Rc<T> для многопоточного кода.
//     RefCell<T> — для внутренней мутабельности с проверками времени выполнения.
//     Mutex<T> — для безопасного доступа к данным в многопоточной среде.
//     RwLock<T> — для множественного чтения или единичной записи в многопоточном коде.

/*** Box<T> ***/

// Когда использовать Box<T>:
// - Когда размер типа неизвестен во время компиляции.
// - Когда у вас есть большой объём данных и вы хотите передать владение без копирования. Например, крупный массив или структура со множеством объёмных полей.
// - Когда требуется владение типом, размер которого неизвестен на этапе компиляции.

pub fn box_using() {
    println!("Начало box_using");
    
    {
        println!("Вход во внутреннюю область видимости");
        let b = Box::new(5);
        println!("b = {}", b);
        println!("Выход из внутренней области видимости");
    } // <- Здесь Box<T> автоматически освобождает память
    
    println!("Продолжение box_using - память уже освобождена");
}


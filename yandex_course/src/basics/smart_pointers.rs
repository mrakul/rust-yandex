/*** Rc<T> ***/

//  Rc<T> (Reference Counting) позволяет нескольким владельцам ссылаться на одни и те же данные. Счётчик ссылок отслеживает количество ссылок, и данные удаляются, когда счётчик достигает нуля.
// (!) Важно учитывать, что Rc<T> работает только в однопоточном коде. 
// Тип Rc<T> используется, когда мы:

// - Хотим разместить в куче некоторые данные для чтения несколькими частями нашей программы.
// - При этом не можем определить во время компиляции, какая из частей завершит использование данных последней.


// Когда нужно, чтобы несколько частей программы могли читать одни и те же данные
// ✅ Да
// Основное назначение Rc<T> — разделяемое владение для только чтения (без мутабельности). Пример: общий конфиг, AST в парсере.

// Когда невозможно на этапе компиляции определить, кто будет последним владельцем данных
// ✅ Да
// Ключевой случай: сложные графы владения, где время жизни определяется динамически (например, узлы дерева с несколькими родителями).

use std::rc::Rc;
enum List {
    Cons(i32, Rc<List>),
    Nil,
}

use List::{Cons, Nil};

pub fn rc_using() {
    let a = Rc::new(Cons(5, Rc::new(Cons(10, Rc::new(Nil)))));

    println!("Счётчик ссылок после создания a: {}", Rc::strong_count(&a));       // 1
    
    let b = Cons(3, Rc::clone(&a));
    println!("Счётчик ссылок после создания b: {}", Rc::strong_count(&a));       // 2
    
    {
        let c = Cons(4, Rc::clone(&a));
        println!("Счётчик ссылок после создания c: {}", Rc::strong_count(&a));   // 3
    }
    
    println!("Счётчик ссылок после выхода c из области видимости: {}", Rc::strong_count(&a)); // 2
}



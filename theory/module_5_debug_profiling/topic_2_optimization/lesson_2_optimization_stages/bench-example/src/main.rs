#![feature(test)]
extern crate test;

use test::Bencher;

fn sum_vec(items: &[u32]) -> u64 {
    items.iter().map(|&x| x as u64).sum()
}

#[bench]
fn bench_sum_1000(b: &mut Bencher) {
    let data: Vec<u32> = (0..1000).collect();
    b.iter(|| sum_vec(&data));
} 

// (!) Запустить nightly-версию однократно можно так: cargo +nightly bench

// Когда вы запускаете cargo bench, Rust выполняет функцию много раз и выводит среднее время на итерацию.
// Формат выглядит так:

// test bench_sum_1000    ... bench:       1,234 ns/iter (+/- 567) 

// Здесь:

//     bench_sum_1000 — имя бенчмарка.
//     1,234 ns/iter — среднее время на одну итерацию (наносекунды).
//     +/- 567 — стандартное отклонение (погрешность измерения).

// Ограничения cargo bench
// Встроенный cargo bench имеет серьёзные ограничения:

//     Результаты могут сильно варьироваться между запусками из-за нагрузки системы, состояния кеша, планировщика ОС.
//     Стандартное отклонение часто составляет 20–30% от среднего, что делает измерения ненадёжными для точных сравнений.
//     Нет проверки на выбросы, нет перцентилей, нет сравнения версий.
//     Вы не можете уверенно сказать, что оптимизация помогла, если разница меньше 50%.
//     Атрибут #\[bench\] доступен только в nightly-версии компилятора, что ограничивает его использование в стабильных проектах.
//     Результаты выводятся только в текстовом виде, нет графиков и сравнений.

// Из-за этих ограничений для серьёзных измерений лучше использовать criterion, который предоставляет статистический анализ и работает на stable Rust.
// Важно понимать, как работает встроенный бенч-харнесс: он выполняет разминку, затем выбирает количество итераций так, чтобы время одной пробы было ощутимо длиннее таймеров ОС, и делит суммарное время на число итераций. Запуски идут в одном потоке, без закрепления на ядре и без управления частотой CPU, поэтому системный шум сильно влияет на результат. Для более честных замеров можно привязать процесс к ядру (taskset), отключить turbo и энергосбережение, выключить фоновые задачи и собирать в release-профиле.
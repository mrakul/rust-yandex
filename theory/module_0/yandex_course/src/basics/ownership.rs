// При возврате значения из функции владение этим значением автоматически передаётся вызывающей стороне.
// Это позволяет избежать преждевременного освобождения данных и обеспечивает гибкость в управлении ресурсами.
fn give_ownership_after_call () {
    // `s1` получает владение
    let s1 = give_ownership(); 

    println!("{}", s1);
}

fn give_ownership() -> String {
    let s = String::from("yandex");
    s
} // <- `s` не освобождается - владение возвращается вызывающей стороне 



// Модель владения в Rust

// Модель владения — это фундаментальный механизм языка Rust, обеспечивающий безопасность памяти без необходимости управлять ею вручную или использовать сборщик мусора.

// Она основана на трёх строгих правилах, контролируемых на этапе компиляции:
//  - Каждое значение в Rust имеет владельца.
//  - В каждый момент времени может быть только один владелец значения.
//  - Когда владелец выходит из области видимости, значение уничтожается.


pub fn simple_ownership() {
    // `x` получает владение
    let x = 1;

    {
        // `y` получает во владение новое значение, потому что примитивы реализуют трейт Copy.
        //  К таким типам относятся все скалярные типы, а также некоторые составные, 
        //  целиком состоящие из Copy-типов. Например, (i32, bool) или [u8; 4].
        let y = x;

        println!("y = {}", y);
    }

    println!("x = {}", x);
}

pub fn string_ownership() {
    // `s1` получет владение
    let s1 = String::from("yandex");

    {
        // Передача владения (move):
        // `s1` теряет владение
        // `s2` получет владение
        let s2 = s1;

        println!("{s2}");
    }

    // ОШИБКА: `s1` - больше не валидна
    // println!("{s1}");
}

pub fn string_clone() {

    // `s1` получает во владение данные
    let s1 = String::from("rust");

    // `s2` получает во владение полную копию `s1`
    // (необходима реализация трейта Clone)
    let s2 = s1.clone();

    // Обе переменные валидны
    println!("{} и {}", s1, s2); 
}

pub fn take_ownership_by_func () {
    // `s1` получает владение
    let s1 = String::from("yandex");

    // Владение передаётся аргументу функции
    take_ownership(s1);

    // ОШИБКА: `s1` больше не владеет данными
    // println!("{}", s1); 
}


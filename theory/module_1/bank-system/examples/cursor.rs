// В Rust часто нужно тестировать функции работы с файлами или потоками данных.
// Но создавать реальные файлы на диске неудобно и медленно.
// Для этого используется тип std::io::Cursor — виртуальный поток, который хранится прямо в памяти. Он может вести себя как файл:
//  - Источник данных (Read) — можно «читать» из памяти так же, как из файла.
//  - Приёмник данных (Write) — можно «записывать» в память так же, как в файл.

use std::io::{Cursor, Read, Write};

fn main() {
    // Создаём буфер в памяти (передаём внутрь Vec)
    let mut buffer = Cursor::new(Vec::new());

    // Записываем данные
    write!(buffer, "Hello, world!").unwrap();

    // Возвращаем курсор в начало, чтобы читать
    buffer.set_position(0);

    // Читаем данные обратно
    let mut output = String::new();
    buffer.read_to_string(&mut output).unwrap();

    println!("Считано из памяти: {}", output);
} 